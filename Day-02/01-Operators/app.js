// 1. Arithmetic Operators

let a = 10, b = 3;
console.log(a + b); // 13
console.log(a - b); // 7
console.log(a * b); // 30
console.log(a / b); // 3.33
console.log(a % b); // 1

// 2. Assignment Operators

let x = 10;
x += 5; // x = 15
console.log(x);

// 3. Comparison Operators

console.log(10 == "10");  // true (value matches)
console.log(10 === "10"); // false (type doesn't match)
console.log(5 > 3);       // true

// 4. Logical Operators

let c = 10;
let d = 5;
console.log(a > 5 && b < 10); // true
console.log(a > 15 || b < 10); // true
console.log(!(a > 5)); // false

// 5. Bitwise Operators

console.log(5 & 1); // 1
console.log(5 | 1); // 5
console.log(5 ^ 1); // 4
console.log(~5);    // -6



